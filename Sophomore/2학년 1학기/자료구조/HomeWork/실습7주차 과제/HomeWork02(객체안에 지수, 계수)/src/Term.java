public class Term {
	private int jisu;
	private int gesu;

	public Term(int gesu, int jisu) {
		this.gesu = gesu;
		this.jisu = jisu;
	}

	public void setGesu(int gesu) {
		this.gesu = gesu;
	}

	public void setJisu(int jisu) {
		this.jisu = jisu;
	}

	public int getJisu() {
		return jisu;
	}

	public int getGesu() {
		return gesu;
	}
}
